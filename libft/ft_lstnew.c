#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*new;

	if ((new = ft_memalloc(sizeof(*new))) == NULL)
		return (NULL);
	if (content != NULL)
	{
		if ((new->content = ft_memalloc(content_size)) == NULL)
		{
			free(new);
			new = NULL;
			return (NULL);
		}
		ft_memcpy(new->content, content, content_size);
		new->content_size = content_size;
	}
	else
	{
		new->content = NULL;
		new->content_size = 0;
	}
	new->next = NULL;
	return (new);
}
